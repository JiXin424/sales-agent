"""成交推进 prompt 模板。"""

DEAL_ADVANCEMENT_PROMPT = """## 任务：成交推进建议

用户希望推进当前销售机会的成交进度，需要系统化的推进策略。

## 严格规则

1. 必须先诊断当前所处的销售阶段，再给出对应阶段的推进策略。
2. 不同阶段的策略必须明显不同：
   - 需求阶段：推进到方案验证
   - 方案阶段：推进到报价/试点
   - 报价阶段：推进到商务谈判
   - 谈判阶段：推进到签约
3. 必须识别具体的成交阻碍（blocker），不能只说"需要更多沟通"。
4. 建议的下一步必须具体可执行，包含话术或动作。
5. 必须列出需要与客户确认的事项。
6. 风险警告必须具体（如"不要在未确认决策人之前逼单"）。
7. 涉及价格、合同、交付时，必须提醒销售确认。
8. 不允许建议欺骗或高压手段。
9. 如果检索到企业知识库内容，用于补充方案/案例/竞争信息并标注来源。

## 输出 JSON 格式

```json
{{
  "summary": "当前销售阶段是...，主要阻碍是...，建议通过...推进",
  "sections": [
    {{"title": "阶段诊断", "content": "根据你的描述，当前处于...阶段"}},
    {{"title": "成交阻碍", "content": "当前阻碍成交的主要问题是：..."}},
    {{"title": "建议下一步", "content": "..."}},
    {{"title": "行动话术", "content": "「...」"}},
    {{"title": "推进理由", "content": "为什么建议这样做：..."}},
    {{"title": "需确认事项", "content": "在执行前，需要先确认：\\n1. ...\\n2. ..."}},
    {{"title": "风险警告", "content": "..."}}
  ]
}}
```

## 用户输入

{message}

{context_block}

{retrieval_block}
"""
