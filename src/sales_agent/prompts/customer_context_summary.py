"""客户上下文整理 prompt 模板。"""

CUSTOMER_CONTEXT_SUMMARY_PROMPT = """## 任务：客户上下文整理

用户提供了一段客户相关的信息（聊天记录、笔记、碎片信息），需要从中提取和规范化客户上下文字段。

## 严格规则

1. 只提取用户信息中**明确提到**的事实，不能推断或编造。
2. 对于信息中未提及的字段，必须标记为「未知」，不能留空或猜测。
3. 「缺失信息」部分必须列出至少 3 个下一步应该了解的关键问题。
4. 客户行业、角色必须从信息中直接获取，不能根据公司名猜测。
5. 预算信号只能标记为"已提及"或"未提及"，不能估算具体数字。
6. 决策流程如果未提及，必须标记为"需确认"。
7. 当前销售阶段如果未明确提及，根据上下文推断后标注「（推断）」。
8. 所有判断必须基于用户提供的信息，不能引入外部知识。
9. 如果检索到企业知识库内容，可用于补充对该客户的已知历史。

## 输出 JSON 格式

```json
{{
  "summary": "基于提供的信息，整理出客户上下文如下...",
  "sections": [
    {{"title": "客户行业", "content": "..."}},
    {{"title": "客户角色/决策人", "content": "..."}},
    {{"title": "核心痛点", "content": "..."}},
    {{"title": "预算信号", "content": "已提及：... / 未提及"}},
    {{"title": "决策流程", "content": "..."}},
    {{"title": "已知异议", "content": "..."}},
    {{"title": "当前销售阶段", "content": "..."}},
    {{"title": "紧迫性", "content": "..."}},
    {{"title": "缺失信息", "content": "以下信息尚未获取，建议尽快了解：\\n1. ...\\n2. ...\\n3. ..."}},
    {{"title": "关键提醒", "content": "..."}}
  ]
}}
```

## 用户输入

{message}

{context_block}

{retrieval_block}
"""
