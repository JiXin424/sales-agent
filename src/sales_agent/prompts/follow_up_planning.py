FOLLOW_UP_PLANNING_PROMPT = """## 任务：跟进计划

用户需要根据与客户的互动情况制定跟进计划，包括时机、方式、话术和备选方案。

## 严格规则

1. 必须根据用户描述的场景判断客户当前的购买信号和风险信号。
2. 建议跟进时机必须具体（如"3天后周三下午"而非"近期"）。
3. 下一步行动必须具体可执行，不能是"继续跟进"这种空话。
4. 消息草稿至少给出 2 个版本（正式版和轻松版），用「」包裹，可直接复制使用。
5. 必须提供备选方案：如果客户仍然不回复或拒绝，下一步怎么办。
6. 不能建议过度骚扰客户（如"每天追问"）。
7. 跟进频率要合理：刚演示完 → 3-5天；报价后 → 1周；长期无回应 → 2-3周用新价值点重新触达。
8. 涉及价格、合同、交付时，必须提醒销售确认。
9. 不允许承诺未确认的条件。
10. 如果检索到企业知识库内容，用于补充产品/方案角度并标注来源。

## 输出 JSON 格式

```json
{{
  "summary": "客户当前处于...状态，建议在...时用...方式跟进",
  "sections": [
    {{"title": "客户状态诊断", "content": "基于你的描述，客户当前状态是..."}},
    {{"title": "购买信号", "content": "..."}},
    {{"title": "风险信号", "content": "..."}},
    {{"title": "建议跟进时机", "content": "..."}},
    {{"title": "下一步行动", "content": "..."}},
    {{"title": "消息草稿-正式版", "content": "「...」"}},
    {{"title": "消息草稿-轻松版", "content": "「...」"}},
    {{"title": "备选方案", "content": "如果客户...，则..."}},
    {{"title": "风险提醒", "content": "..."}}
  ]
}}
```

## 用户输入

{message}

{context_block}

{retrieval_block}
"""
